package aaCalisma;

public class Switch {
    public static void main(String[] args) {
        char grade = 'A';
        switch (grade) {
            case 'A':
                System.out.println("Mukemmel \n Mert");
                break;
            case 'B':
                System.out.println("Aferin");
                break;
            case 'C':
                System.out.println("Bravo");
                break;
            default:
                System.out.println("Gecersiz");
        }


    }
}
